    for i in curve_picked:

        if i[0] <= x_input <= curve_picked[curve_picked.index(i) + 1][0]:
            x1 = i[0]
            x2 = curve_picked[curve_picked.index(i) + 1][0]

            y1 = i[1]
            y2 = curve_picked[curve_picked.index(i) + 1][1]

            k = (x_input - x1) / (x2 - x1)

            return (1 - k) * y1 + k * y2


